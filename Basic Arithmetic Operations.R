#1. Basic Arithmetic Operations:
#Addition
a <- 5
b <- 10
c <- a + b
print(c)

#Subtraction
a <- 5
b <- 10
c <- b - a
print(c)

#Multiplication
a <- 5
b <- 10
c <- a * b
print(c)

#Division
a <- 5
b <- 10
c <- b / a
print(c)

#2. Logical Operations:
#AND
a <- TRUE
b <- FALSE
c <- a & b
print(c)

#OR
a <- TRUE
b <- FALSE
c <- a | b
print(c)

#NOT
a <- TRUE
c <- !a
print(c)

#3. Comparison Operators:
#Greater than
a <- 5
b <- 10
c <- b > a
print(c)

#Less than
a <- 5
b <- 10
c <- a < b
print(c)

#Equal to
a <- 5
b <- 10
c <- a == b
print(c)

#Not equal to
a <- 5
b <- 10
c <- a != b
print(c)